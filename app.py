from flask import Flask, request, render_template_string, redirect, url_for
import pandas as pd
import os

app = Flask(__name__)

# Caminho do arquivo no servidor
file_path = 'teste 1.xlsx'

# Verifica se o arquivo existe
if os.path.exists(file_path):
    # Carregar a planilha Excel
    df = pd.read_excel(file_path)
else:
    df = pd.DataFrame(columns=["Palavras chaves", "T√≠tulo do documento", "Link Qualyteam", "Resumo"])

# Emoji de rosto humano
face_emoji = "üë§"

def search_in_spreadsheet(term):
    term = term.lower()  # Converte a entrada do usu√°rio para min√∫sculas
    results = df[df['Palavras chaves'].str.contains(term, case=False, na=False)]
    if not results.empty:
        return results[['T√≠tulo do documento', 'Link Qualyteam', 'Resumo']].to_dict('records')
    else:
        return []

@app.route('/', methods=['GET', 'POST'])
def home():
    # Inicializa o hist√≥rico de chat a cada nova sess√£o
    chat_history = ["ü§ñ Emabot: Ol√°, eu sou a Emabot da Diplan. Sua assistente de busca... Como posso ajudar? Fale comigo somente por palavras-chave. Exemplo: Processos.."]

    if request.method == 'POST':
        user_input = request.form['user_input'].strip().lower()  # Converte a entrada do usu√°rio para min√∫sculas e remove espa√ßos em branco
        
        # Substitui o texto do usu√°rio pelo emoji de rosto humano
