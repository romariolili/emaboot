# Fun√ß√£o para normalizar o texto, removendo acentua√ß√£o e convertendo para min√∫sculas
def normalize(text):
    return unidecode(text.strip().lower()) if text else ""

# Fun√ß√£o de busca na planilha focada em palavras-chave
def search_in_spreadsheet(term):
    normalized_term = normalize(term)

    # Filtra as linhas onde a palavra-chave normalizada est√° contida em "Palavras chaves"
    results = df[df['Palavras chaves'].apply(lambda x: normalized_term in normalize(str(x)))]

    if not results.empty:
        return results[['T√≠tulo do documento', 'Link Qualyteam', 'Resumo', 'Data elabora√ß√£o']].to_dict('records')
    else:
        return []

# Fun√ß√£o para inicializar o hist√≥rico de chat na sess√£o
def initialize_chat_history():
    # Inicializa o hist√≥rico de chat na sess√£o se ainda n√£o estiver presente
    session['chat_history'] = [
        "ü§ñ Emabot: Ol√°, me chamo Emaboot da Diplan. Sou sua assistente de busca de documentos. Como posso ajudar? Digite uma palavra-chave."
    ]
    return session['chat_history']

# Rota principal
@app.route('/', methods=['GET', 'POST'])
def home():
    # Sempre inicializa o hist√≥rico de chat ao carregar a p√°gina
    if request.method == 'GET':
        # Limpa o hist√≥rico de chat na sess√£o
        session.pop('chat_history', None)
        initialize_chat_history()

    chat_history = session.get('chat_history', [])

    if request.method == 'POST':
        user_input = request.form['user_input'].strip()

        # Verifica se a entrada cont√©m mais de uma palavra (indicando uma frase)
        if len(user_input.split()) > 1:
            chat_history.append(f"{face_emoji}: {user_input}")
            chat_history.append("ü§ñ Emabot: Por favor, use apenas palavras-chave para realizar a busca.")
        else:
            if user_input:
                chat_history.append(f"{face_emoji}: {user_input}")
                results = search_in_spreadsheet(user_input)
                if results:
                    chat_history.append("ü§ñ Emabot: Documentos encontrados:")
                    for result in results:
                        chat_history.append(f"üìÑ <a href='/get_link?title={result['T√≠tulo do documento']}'>{result['T√≠tulo do documento']}</a>")
                else:
                    chat_history.append("ü§ñ Emabot: Nenhum documento encontrado com a palavra-chave fornecida.")
            else:
                chat_history.append("ü§ñ Emabot: Por favor, insira uma palavra-chave para realizar a busca.")

