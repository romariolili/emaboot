from flask import Flask, request, render_template_string, redirect, url_for
import pandas as pd
import os

app = Flask(__name__)

# Caminho do arquivo no servidor
file_path = 'teste 1.xlsx'

# Verifica se o arquivo existe
if os.path.exists(file_path):
    # Carregar a planilha Excel
    df = pd.read_excel(file_path)
else:
    df = pd.DataFrame(columns=["Palavras chaves", "T√≠tulo do documento", "Link Qualyteam"])

# Emoji de rosto humano
face_emoji = "üë§"

# Inicializa o hist√≥rico de chat como uma lista vazia
chat_history = []

# Vari√°veis para armazenar nome e setor do usu√°rio
user_name = None
user_sector = None
collecting_name = True
collecting_sector = False

def search_in_spreadsheet(term):
    results = df[df['Palavras chaves'].str.contains(term, case=False, na=False)]
    if not results.empty:
        return results[['T√≠tulo do documento', 'Link Qualyteam']].to_dict('records')
    else:
        return []

@app.route('/', methods=['GET', 'POST'])
def home():
    global chat_history, user_name, user_sector, collecting_name, collecting_sector

    if request.method == 'POST':
        user_input = request.form['user_input']
        
        # Substitui o texto do usu√°rio pelo emoji de rosto humano
        user_message = f"{face_emoji}: {user_input}"
        chat_history.append(user_message)  # Adiciona a intera√ß√£o atual ao hist√≥rico
        
        if collecting_name:
            user_name = user_input
            collecting_name = False
            collecting_sector = True
            chat_history.append("ü§ñ Emabot: Qual seu setor?")
        elif collecting_sector:
            user_sector = user_input
            collecting_sector = False
            chat_history.append(f"ü§ñ Emabot: Obrigado, {user_name}, do setor {user_sector}. Sou sua assistente de busca... Como posso ajudar? Fale comigo somente por palavras-chave. Exemplo: Processos..")
        else:
            results = search_in_spreadsheet(user_input)
            if results:
                chat_history.append("ü§ñ Emabot: Documentos encontrados:")
                for result in results:
                    chat_history.append(f"üìÑ <a href='/get_link?title={result['T√≠tulo do documento']}'> {result['T√≠tulo do documento']}</a>")
            else:
                chat_history.append("ü§ñ Emabot: Nenhum documento encontrado com essas palavras-chave.")
    
    # Verifica se √© a primeira intera√ß√£o e inicia com a sauda√ß√£o se necess√°rio
    if not chat_history:
        chat_history.append("ü§ñ Emabot: Ol√°, me chamo Emaboot da Diplan, qual seu nome?")
    
    return render_template_string('''
        <h1>Emabot da Diplan</h1>
        <div style="border:1px solid #ccc; padding:10px; margin-bottom:10px;">
            {% for message in chat_history %}
                <p>{{ message | safe }}</p>
            {% endfor %}
        </div>
        <form method="post" action="/">
            <label for="user_input">Digite sua mensagem:</label><br>
            <input type="text" id="user_input" name="user_input" style="width:80%">
            <input type="submit" value="Enviar">
        </form>
    ''', chat_history=chat_history)

@app.route('/get_link', methods=['GET'])
def get_link():
    global chat_history  # Certifique-se de que o hist√≥rico de chat seja acess√≠vel
    title = request.args.get('title')
    result = df[df['T√≠tulo do documento'] == title]
    if not result.empty:
        link = result['Link Qualyteam'].values[0]
        chat_history.append(f"ü§ñ Emabot: Aqui est√° o link para '{title}': <a href='{link}' target='_blank'>{link}</a>")
    else:
        chat_history.append("ü§ñ Emabot: Link n√£o encontrado para o t√≠tulo selecionado.")
    
    # Redireciona de volta para a p√°gina principal para manter o fluxo de intera√ß√£o
    return redirect(url_for('home'))

if __name__ == "__main__":
    # Inicializa a conversa com a nova sauda√ß√£o
    chat_history = ["ü§ñ Emabot: Ol√°, me chamo Emaboot da Diplan, qual seu nome?"]
    app.run(host="0.0.0.0", port=int(os.environ.get("PORT", 5000)))
