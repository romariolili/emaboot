import os
from flask import Flask, request, jsonify, render_template_string
import pandas as pd

app = Flask(__name__)

# Caminho do arquivo Excel
file_path = 'teste 1.xlsx'

# Carregar a planilha Excel
df = pd.read_excel(file_path)

def search_in_spreadsheet(term):
    if not isinstance(term, str) or not term:
        return []
    results = df[df['Palavras chaves'].str.contains(term, case=False, na=False)]
    if not results.empty:
        return results['TÃ­tulo do documento'].tolist()
    else:
        return []

def get_link_by_title(title):
    result = df[df['TÃ­tulo do documento'] == title]
    if not result.empty:
        return result['Link Qualyteam'].values[0]
    else:
        return None

@app.route('/')
def home():
    return render_template_string(open('index.html').read(), response=None)

@app.route('/chatbot', methods=['POST'])
def chatbot_interaction():
    user_input = request.form.get("input", "")
    
    term = user_input.strip()
    results = search_in_spreadsheet(term)
    
    if results:
        response = "ðŸ¤– Emaboot: Documentos encontrados:<br>"
        for i, title in enumerate(results, 1):
            response += f"{i}. {title}<br>"
        return render_template_string(open('index.html').read(), response=response)
    else:
        return render_template_string(open('index.html').read(), response="ðŸ¤– Emaboot: Nenhum documento encontrado com essas palavras-chave.")

@app.route('/get_link', methods=['POST'])
def get_link():
    title = request.form.get("title", "")
    link = get_link_by_title(title)
    
    if link:
        return jsonify({"response": f"ðŸ¤– Emaboot: Aqui estÃ¡ o link para '{title}': {link}"})
    else:
        return jsonify({"response": "ðŸ¤– Emaboot: Link nÃ£o encontrado para o tÃ­tulo selecionado."})

if __name__ == "__main__":
    app.run(host="0.0.0.0", port=int(os.environ.get("PORT", 5000)))
