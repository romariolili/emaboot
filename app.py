def search_in_spreadsheet(term):
    results = df[df['Palavras chaves'].str.contains(term, case=False, na=False)]
    if not results.empty:
        return results[['TÃ­tulo do documento', 'Link Qualyteam', 'Resumo']].to_dict('records')
    else:
        return []

@app.route('/', methods=['GET', 'POST'])
def home():
    chat_history = initialize_chat_history()  # Reinicia a conversa ao recarregar a pÃ¡gina

    if request.method == 'POST':
        user_input = request.form['user_input'].strip()

        if user_input:  # Verifica se o input nÃ£o estÃ¡ vazio
            if len(user_input.split()) > 1:  # Verifica se o usuÃ¡rio digitou mais de uma palavra
                chat_history.append(f"{face_emoji}: {user_input}")
                chat_history.append("ðŸ¤– Emabot: SÃ³ consigo realizar a busca por palavra-chave.")
            else:
                # Adiciona a entrada do usuÃ¡rio ao histÃ³rico
                chat_history.append(f"{face_emoji}: {user_input}")
                
                # Busca nos documentos
                results = search_in_spreadsheet(user_input)
                if results:
                    chat_history.append("ðŸ¤– Emabot: Documentos encontrados:")
                    for result in results:
                        chat_history.append(f"ðŸ“„ <a href='/get_link?title={result['TÃ­tulo do documento']}'>{result['TÃ­tulo do documento']}</a>")
                else:
                    chat_history.append("ðŸ¤– Emabot: Nenhum documento encontrado com essas palavras-chave.")
        else:
            chat_history.append("ðŸ¤– Emabot: Por favor, insira uma palavra-chave para realizar a busca.")
        
        # Armazena o histÃ³rico no cookie
        response = make_response(render_template_string(chat_template, chat_history=chat_history))
        response.set_cookie('chat_history', json.dumps(chat_history))
        return response

    # Ao recarregar, limpa o histÃ³rico de chat (resetando a conversa)
    response = make_response(render_template_string(chat_template, chat_history=chat_history))
    response.set_cookie('chat_history', '', expires=0)  # Limpa o cookie
    return response

